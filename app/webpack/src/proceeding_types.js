import $ from 'jquery'
import * as ProceedingTypes from './modules/proceedings'

$(function() {
  if ($("#proceeding-search-input").length){ searchProceedingTypes() }
})

function searchProceedingTypes(){
  ProceedingTypes.getAll().then(proceedings_data => ProceedingTypes.filterSearch(proceedings_data))
}

export { searchProceedingTypes }
